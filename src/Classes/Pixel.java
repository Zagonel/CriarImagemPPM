package Classes;

public class Pixel {

    private int Red = 0;
    private int Blue = 0;
    private int Grean = 0;

    public int getRed() {
        return Red;
    }

    public void setRed(int red) {
        Red = red;
    }

    public int getBlue() {
        return Blue;
    }

    public void setBlue(int blue) {
        Blue = blue;
    }

    public int getGrean() {
        return Grean;
    }

    public void setGrean(int grean) {
        Grean = grean;
    }
}
